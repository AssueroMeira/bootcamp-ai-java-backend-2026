public class VariaveisTipos {

    public static void main(String[] args) {

        int idade = 35;
        double altura = 1.75;
        char letraInicial = 'A';
        boolean estudante = true;

        String nome = "Assuero";

        System.out.println("Nome: " + nome);
        System.out.println("Idade: " + idade);
        System.out.println("Altura: " + altura);
        System.out.println("Letra inicial: " + letraInicial);
        System.out.println("É estudante? " + estudante);
    }
}

/*
** Este código demonstra a declaração e uso de variáveis em Java,
** incluindo tipos primitivos (int, double, char, boolean) e o tipo String.
** Em java, as variáveis devem ser declaradas com um tipo específico, e o tipo String é usado para representar texto.
** Modelo mental:
** tipo + nomeDaVariavel + valor;
*/